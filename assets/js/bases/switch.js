
const dia = 0; //0 domingo

// el switch evalua con el ===

switch (dia) {
    case 0:
        console.log('Domingo');
        break;
    case 1:
        console.log('Lunes');
        break;
    case 2:
        console.log('Martes');
        break;

    default:
        console.log('no es dia de la semana');
        break;
}